--Q.14 FIND THE TOP 10 ACTORS WHO HAVE APPEARED IN THE HIGHEST NUMBER OF MOVIES PRODUCED IN INDIA.
SELECT
UNNEST(STRING_TO_ARRAY(casts,',')) AS ACTOR,
COUNT(CASE WHEN net_type='Movie' THEN 1 END) AS TOTAL_COUNT
FROM netflix
WHERE country ILIKE '%India%'
GROUP BY ACTOR
ORDER BY TOTAL_COUNT
DESC LIMIT 10;

/*Q.15 CATEGORIZE THE CONTENT BASED ON THE PRESENCE OF KEYWORDS 'KILL' AND 'VIOLENCE' IN
THE DESCRIPTION FIELD.LABEL CONTENT CONTAINING THESE KEYWORDS AS 'BAD' AND ALL OTHER
CONTENT AS 'GOOD'.COUNT HOW MANY ITEMS FALL INTO EACH CATEGORY. */

WITH NEW_TABLE
AS
(
SELECT *,
  CASE
  WHEN
     description ILIKE '%kill%'
     OR
     description ILIKE '%violence%'
     THEN 'BAD CONTENT'
     ELSE 'GOOD CONTENT'
  END CATEGORY
FROM netflix
)
SELECT
      category,
	  COUNT(category) AS TOTAL_COUNT
	  FROM NEW_TABLE
	  GROUP BY 1;