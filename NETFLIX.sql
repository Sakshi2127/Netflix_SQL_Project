--Q.4 FIND THE TOP 5 COUNTRIES WITH THE MOST CONTENT ON NETFLIX
SELECT * FROM netflix;
SELECT 
UNNEST(STRING_TO_ARRAY(country,',')) AS NEW_COUNTRY,
COUNT(show_id) AS NEW_COUNT
FROM netflix
GROUP BY NEW_COUNTRY
ORDER BY NEW_COUNT
DESC
LIMIT 5;

