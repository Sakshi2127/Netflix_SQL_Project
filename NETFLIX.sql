/*Q.10 FIND EACH YEAR AND THE AVERAGE NUMBERS OF CONTENT RELEASED IN INDIA ON NETFLIX.
RETURN TOP 5 YEAR WITH HIGHEST AVG CONTENT RELEASE !*/
SELECT 
EXTRACT(YEAR FROM TO_DATE(date_added,'Month DD, YYYY')) AS YEAR,
COUNT(*)
FROM NETFLIX
WHERE country='India'
GROUP BY YEAR;

